SELECT 	product_name AS "Product", AVG(price) AS 'Avg Price'
FROM 	Price_History
WHERE 	product_name = 'iPhone XS' AND
start_date >= '2018-08-01' AND
end_date <= '2018-08-31'
GROUP BY 	product_name;

